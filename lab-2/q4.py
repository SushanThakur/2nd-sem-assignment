stack = []
queue = []

# Stack operations
stack.append(1)
stack.append(2)
stack.append(3)
print("Stack after push:", stack)
stack.pop()
print("Stack after pop:", stack)

# Queue operations
queue.append(1)
queue.append(2)
queue.append(3)
print("Queue after enqueue:", queue)
queue.pop(0)
print("Queue after dequeue:", queue)

